int	ft_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb > 0)
	{
		while (nb / a >= a)
		{
			a++;
			if (nb / a == a && nb % a == 0)
				return (a);
		}
	}
	return (0);
}
